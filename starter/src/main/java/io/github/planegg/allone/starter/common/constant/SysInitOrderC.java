public class SysInitOrderC {
    /**
     * 加载顺序起始值
     */
    private final static int ORDER_NUM_START = 10000;
    /**
     * 加载顺序步进值
     */
    private final static int ORDER_NUM_STEP = 100;
    /**
     * 加载顺序步进值
     */
    public final static int ORDER_NUM_BEAN =  ORDER_NUM_START + ORDER_NUM_STEP * 1;
    /**
     * 加载顺序步进值
     */
    public final static int ORDER_NUM_AOP =  ORDER_NUM_START + ORDER_NUM_STEP * 2;





}
